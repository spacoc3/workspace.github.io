let a = 10;
//operadores relacionales 
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

//operadores de igualdad

console.log(a == 10);// a igual a 10
console.log(a != 10);// (!)a distinto o diferente de 10
console.log(a == '10');// solo consulta por el tipo de valor 

//son los operadores mas usados y mas recomendados--->

console.log(a === '10');// (===)consulta si el tipo de valor aparte del tipo de dato son iguales 
console.log(a !== '10');// (!==)consulta si son distintos tipos de valor o distintos tipos de dato 

